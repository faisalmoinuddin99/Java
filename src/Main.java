import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
     /*
     do While Loop: Execute at least one time

     while - check condition then execute
     do while - execute then check condition
      */
        int b = 10 ;
        do{
            System.out.println(b);
            b++;
        } while (b < 5) ;
    }
}

