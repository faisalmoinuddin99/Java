import java.util.Scanner;

public class Main {
    public static void main(String[] args) {

       // Bitwise Operator
        /*
        * 2 --- 10 in Binary
        * 3 --- 11 in Binary
        * AND ------
        *       10 --> 2
        * OR --------
        *        11 --> 3
        * */
        System.out.println(2 & 3);
        System.out.println(2 | 3);

    }
}

